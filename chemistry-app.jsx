// Unit detail view
// ============================================================
function UnitView({ unit, progress, onUpdate, onBack, onNext }) {
  const [mode, setMode] = useState('learn'); // learn | practice | test
  const [blankIdx, setBlankIdx] = useState(0);
  const [showConfetti, setShowConfetti] = useState(false);

  const handlePracticePass = async (seconds) => {
    setShowConfetti(true);
    setTimeout(() => setShowConfetti(false), 2200);
    const np = { ...progress };
    np.practiced = (np.practiced || 0) + 1;
    await onUpdate(np);
  };

  const handleTestPass = async (pct) => {
    const np = { ...progress };
    np.passed = true;
    np.bestScore = Math.max(np.bestScore || 0, pct);
    np.testsPassed = (np.testsPassed || 0) + 1;
    if (!np.mark) np.mark = 'A';
    await onUpdate(np);
    setMode('learn');
  };

  const nextBlank = () => {
    if (blankIdx + 1 < unit.fillBlanks.length) setBlankIdx(blankIdx + 1);
    else {
      // completed all blanks — bump review count
      const np = { ...progress };
      np.reviewCount = (np.reviewCount || 0) + 1;
      onUpdate(np);
      setMode('learn');
    }
  };

  return (
    <div style={{ padding: '16px', maxWidth: 900, margin: '0 auto', minHeight: '100vh', background: unit.bgColor }}>
      <Confetti show={showConfetti} />
      
      {/* Header */}
      <div style={{ display: 'flex', alignItems: 'center', gap: 12, marginBottom: 20, padding: '16px 20px',
        background: '#fff', borderRadius: 18, boxShadow: '0 4px 12px rgba(0,0,0,.06)' }}>
        <button onClick={onBack} style={{ padding: '6px 14px', fontSize: 14, border: 'none', borderRadius: 16, background: '#f3f4f6', cursor: 'pointer', fontWeight: 600 }}>
          ← 単元表
        </button>
        <div style={{ width: 48, height: 48, borderRadius: 12, background: unit.color, color: '#fff',
          display: 'flex', alignItems: 'center', justifyContent: 'center', fontWeight: 900, fontSize: 22 }}>
          {unit.symbol}
        </div>
        <div style={{ flex: 1 }}>
          <div style={{ fontSize: 22, fontWeight: 800, color: '#111' }}>{unit.name}</div>
          <div style={{ fontSize: 12, color: '#666' }}>
            復習: {progress.reviewCount || 0}回 / テスト合格: {progress.testsPassed || 0}回 {progress.bestScore ? `/ 最高 ${progress.bestScore}%` : ''}
          </div>
        </div>
      </div>

      {/* Tabs */}
      <div style={{ display: 'flex', gap: 8, marginBottom: 16, background: '#fff', padding: 6, borderRadius: 14 }}>
        {[
          { key: 'learn', label: '📘 学ぶ' },
          { key: 'practice', label: '✏️ 練習' },
          { key: 'test', label: '📝 確認テスト' }
        ].map(t => (
          <button key={t.key} onClick={() => { setMode(t.key); setBlankIdx(0); }}
            style={{
              flex: 1, padding: '10px 0', fontSize: 14, fontWeight: 700, border: 'none', borderRadius: 10,
              background: mode === t.key ? unit.color : 'transparent',
              color: mode === t.key ? '#fff' : '#666',
              cursor: 'pointer', transition: 'all .2s'
            }}>
            {t.label}
          </button>
        ))}
      </div>

      {/* Content */}
      {mode === 'learn' && (
        <div style={{ display: 'flex', flexDirection: 'column', gap: 16 }}>
          <AudioPlaceholder unitName={unit.name} />

          <div style={{ background: '#fff', borderRadius: 16, padding: 20, boxShadow: '0 4px 12px rgba(0,0,0,.06)' }}>
            <div style={{ fontSize: 15, color: '#444', lineHeight: 1.7, fontStyle: 'italic',
              padding: '14px 18px', background: unit.bgColor, borderLeft: `4px solid ${unit.color}`, borderRadius: 8 }}>
              💡 {unit.intro}
            </div>
          </div>

          <div style={{ background: '#fff', borderRadius: 16, padding: 20, boxShadow: '0 4px 12px rgba(0,0,0,.06)' }}>
            <div style={{ fontSize: 18, fontWeight: 800, marginBottom: 12, color: unit.color }}>🔑 重要ポイント</div>
            <ul style={{ margin: 0, paddingLeft: 20, lineHeight: 1.8, fontSize: 15 }}>
              {unit.keyPoints.map((kp, i) => <li key={i} style={{ marginBottom: 6 }}>{kp}</li>)}
            </ul>
          </div>

          <div style={{ background: '#fff', borderRadius: 16, padding: 20, boxShadow: '0 4px 12px rgba(0,0,0,.06)', overflowX: 'auto' }}>
            <div style={{ fontSize: 18, fontWeight: 800, marginBottom: 12, color: unit.color }}>📊 {unit.table.title}</div>
            <table style={{ width: '100%', borderCollapse: 'collapse', fontSize: 14 }}>
              <thead>
                <tr>{unit.table.headers.map((h, i) => (
                  <th key={i} style={{ padding: '10px 12px', background: unit.color, color: '#fff', textAlign: 'left', borderRadius: i === 0 ? '8px 0 0 0' : i === unit.table.headers.length - 1 ? '0 8px 0 0' : 0 }}>{h}</th>
                ))}</tr>
              </thead>
              <tbody>
                {unit.table.rows.map((row, ri) => (
                  <tr key={ri} style={{ background: ri % 2 ? unit.bgColor : '#fff' }}>
                    {row.map((c, ci) => (
                      <td key={ci} style={{ padding: '10px 12px', borderBottom: '1px solid #eee' }}>{c}</td>
                    ))}
                  </tr>
                ))}
              </tbody>
            </table>
          </div>

          <div style={{ display: 'flex', gap: 12, marginTop: 4 }}>
            <button onClick={() => setMode('practice')}
              style={{ flex: 1, padding: 16, fontSize: 16, fontWeight: 800, border: 'none', borderRadius: 14,
                background: `linear-gradient(135deg, ${unit.color}, ${unit.color}cc)`, color: '#fff', cursor: 'pointer',
                boxShadow: `0 4px 14px ${unit.color}66` }}>
              ✏️ 練習問題に挑戦！
            </button>
            <button onClick={() => setMode('test')}
              style={{ flex: 1, padding: 16, fontSize: 16, fontWeight: 800, border: 'none', borderRadius: 14,
                background: '#fff', color: unit.color, border: `2px solid ${unit.color}`, cursor: 'pointer' }}>
              📝 確認テスト
            </button>
          </div>
        </div>
      )}

      {mode === 'practice' && (
        <div>
          <div style={{ marginBottom: 12, padding: '10px 16px', background: '#fff', borderRadius: 12, fontSize: 14, color: '#666', display: 'flex', justifyContent: 'space-between' }}>
            <span>練習 {blankIdx + 1} / {unit.fillBlanks.length}</span>
            <span>チャレンジ: 語群から選んで空欄にタップで入れよう</span>
          </div>
          <FillBlanks
            key={blankIdx}
            blank={unit.fillBlanks[blankIdx]}
            onPass={handlePracticePass}
            onFail={() => {}}
          />
          <div style={{ display: 'flex', gap: 10, marginTop: 16 }}>
            <button onClick={() => blankIdx > 0 && setBlankIdx(blankIdx - 1)}
              disabled={blankIdx === 0}
              style={{ flex: 1, padding: 14, fontSize: 15, fontWeight: 700, border: 'none', borderRadius: 14,
                background: '#fff', color: blankIdx === 0 ? '#ccc' : unit.color,
                cursor: blankIdx === 0 ? 'not-allowed' : 'pointer', border: `2px solid ${blankIdx === 0 ? '#eee' : unit.color}` }}>
              ← 前へ
            </button>
            <button onClick={nextBlank}
              style={{ flex: 2, padding: 14, fontSize: 15, fontWeight: 700, border: 'none', borderRadius: 14,
                background: `linear-gradient(135deg, ${unit.color}, ${unit.color}cc)`, color: '#fff', cursor: 'pointer' }}>
              {blankIdx + 1 === unit.fillBlanks.length ? '練習完了 → 単元表へ' : '次の練習へ ➜'}
            </button>
          </div>
        </div>
      )}

      {mode === 'test' && (
        <QuizComponent
          quiz={unit.quiz}
          unitName={unit.name}
          onPassTest={handleTestPass}
          onBack={() => setMode('learn')}
        />
      )}

      {/* Bottom Navigation */}
      {mode === 'learn' && (
        <div style={{ display: 'flex', gap: 10, marginTop: 20 }}>
          <button onClick={onBack}
            style={{ flex: 1, padding: 14, fontSize: 15, fontWeight: 700, border: 'none', borderRadius: 14,
              background: '#fff', color: '#333', cursor: 'pointer', boxShadow: '0 2px 8px rgba(0,0,0,.06)' }}>
            📋 単元表に戻る
          </button>
          <button onClick={onNext}
            style={{ flex: 1, padding: 14, fontSize: 15, fontWeight: 700, border: 'none', borderRadius: 14,
              background: `linear-gradient(135deg, ${unit.color}, ${unit.color}cc)`, color: '#fff', cursor: 'pointer',
              boxShadow: `0 4px 12px ${unit.color}55` }}>
            次の単元へ ➜
          </button>
        </div>
      )}
    </div>
  );
}

