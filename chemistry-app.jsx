// Fill-in-the-blank exercise (tap bank word → blanks)
// ============================================================
function FillBlanks({ blank, onPass, onFail }) {
  const [blanks, setBlanks] = useState(blank.answers.map(() => null));
  const [bankUsed, setBankUsed] = useState(blank.bank.map(() => false));
  const [judged, setJudged] = useState(false);
  const [result, setResult] = useState(null); // 'pass' | 'fail' | null
  const [elapsed, setElapsed] = useState(0);
  const startRef = useRef(Date.now());
  const timerRef = useRef(null);

  useEffect(() => {
    timerRef.current = setInterval(() => {
      setElapsed(Math.floor((Date.now() - startRef.current) / 1000));
    }, 1000);
    return () => clearInterval(timerRef.current);
  }, []);

  // Render text with <blank>s
  const parts = blank.text.split(/\{(\d+)\}/);

  const placeWord = (bankIdx) => {
    if (bankUsed[bankIdx] || judged) return;
    // find first empty blank
    const firstEmpty = blanks.findIndex(b => b === null);
    if (firstEmpty === -1) return;
    const newBlanks = [...blanks];
    newBlanks[firstEmpty] = { word: blank.bank[bankIdx], bankIdx };
    setBlanks(newBlanks);
    const nu = [...bankUsed];
    nu[bankIdx] = true;
    setBankUsed(nu);
  };

  const removeFromBlank = (blankIdx) => {
    if (judged && result === 'pass') return;
    if (blanks[blankIdx] === null) return;
    const bIdx = blanks[blankIdx].bankIdx;
    const newBlanks = [...blanks];
    newBlanks[blankIdx] = null;
    setBlanks(newBlanks);
    const nu = [...bankUsed];
    nu[bIdx] = false;
    setBankUsed(nu);
  };

  const judge = () => {
    if (blanks.some(b => b === null)) return;
    clearInterval(timerRef.current);
    setJudged(true);
    const allCorrect = blanks.every((b, i) => b.word === blank.answers[i]);
    if (allCorrect) {
      setResult('pass');
      onPass && onPass(elapsed);
    } else {
      setResult('fail');
      // move incorrect answers back to bank after a short delay
      setTimeout(() => {
        const newBlanks = [...blanks];
        const nu = [...bankUsed];
        blanks.forEach((b, i) => {
          if (b && b.word !== blank.answers[i]) {
            nu[b.bankIdx] = false;
            newBlanks[i] = null;
          }
        });
        setBlanks(newBlanks);
        setBankUsed(nu);
        setJudged(false);
        setResult(null);
        startRef.current = Date.now() - elapsed * 1000;
        timerRef.current = setInterval(() => {
          setElapsed(Math.floor((Date.now() - startRef.current) / 1000));
        }, 1000);
        onFail && onFail();
      }, 1800);
    }
  };

  return (
    <div style={{ background: '#fff', borderRadius: 20, padding: '24px 20px', boxShadow: '0 6px 24px rgba(0,0,0,.08)' }}>
      <div style={{ display: 'flex', justifyContent: 'space-between', marginBottom: 12 }}>
        <span style={{ fontSize: 14, color: '#666' }}>⏱ {elapsed}秒</span>
        <span style={{ fontSize: 14, color: '#666' }}>空欄 {blanks.filter(b => b !== null).length} / {blanks.length}</span>
      </div>

      {/* Problem text with blanks */}
      <div style={{ fontSize: 17, lineHeight: 2, marginBottom: 20, whiteSpace: 'pre-wrap' }}>
        {parts.map((p, i) => {
          if (i % 2 === 0) return <span key={i}>{p}</span>;
          const blankIdx = parseInt(p);
          const filled = blanks[blankIdx];
          const isCorrect = judged && filled && filled.word === blank.answers[blankIdx];
          const isWrong = judged && filled && filled.word !== blank.answers[blankIdx];
          return (
            <span
              key={i}
              onClick={() => removeFromBlank(blankIdx)}
              style={{
                display: 'inline-block',
                minWidth: 80,
                padding: '4px 12px',
                margin: '0 4px',
                borderRadius: 10,
                border: filled ? '2px solid #10B981' : '2px dashed #ccc',
                background: isCorrect ? '#D1FAE5' : isWrong ? '#FEE2E2' : filled ? '#fff' : '#fafafa',
                color: isCorrect ? '#065F46' : isWrong ? '#991B1B' : '#333',
                textAlign: 'center',
                fontWeight: 600,
                cursor: filled && !judged ? 'pointer' : 'default',
                transition: 'all .2s'
              }}
            >
              {filled ? filled.word : '　'}
            </span>
          );
        })}
      </div>

      {/* Bank */}
      <div style={{ marginBottom: 16 }}>
        <div style={{ fontSize: 13, color: '#888', marginBottom: 8 }}>語群 (タップして空欄へ)</div>
        <div style={{ display: 'flex', flexWrap: 'wrap', gap: 8 }}>
          {blank.bank.map((word, i) => (
            <button
              key={i}
              onClick={() => placeWord(i)}
              disabled={bankUsed[i]}
              style={{
                padding: '8px 14px',
                fontSize: 15,
                border: 'none',
                borderRadius: 20,
                cursor: bankUsed[i] ? 'not-allowed' : 'pointer',
                background: bankUsed[i] ? '#e5e7eb' : 'linear-gradient(135deg, #A5F3FC, #67E8F9)',
                color: bankUsed[i] ? '#9ca3af' : '#164E63',
                opacity: bankUsed[i] ? 0.4 : 1,
                fontWeight: 600,
                boxShadow: bankUsed[i] ? 'none' : '0 2px 6px rgba(0,0,0,.08)',
                transition: 'all .2s'
              }}
            >
              {word}
            </button>
          ))}
        </div>
      </div>

      {/* Judge button & feedback */}
      <div style={{ display: 'flex', alignItems: 'center', gap: 16 }}>
        <button
          onClick={judge}
          disabled={blanks.some(b => b === null) || (judged && result === 'pass')}
          style={{
            padding: '12px 28px',
            fontSize: 16,
            fontWeight: 700,
            border: 'none',
            borderRadius: 30,
            background: blanks.some(b => b === null) ? '#ccc' : 'linear-gradient(135deg, #F59E0B, #EF4444)',
            color: '#fff',
            cursor: blanks.some(b => b === null) ? 'not-allowed' : 'pointer',
            boxShadow: '0 4px 12px rgba(245,158,11,.3)'
          }}
        >
          ✓ 判定
        </button>
        {result === 'pass' && (
          <div style={{ color: '#10B981', fontWeight: 700, fontSize: 18 }}>
            🎉 合格！ ({elapsed}秒)
          </div>
        )}
        {result === 'fail' && (
          <div style={{ color: '#EF4444', fontWeight: 700, fontSize: 18 }}>
            残念！間違っている空欄の語句を戻します…
          </div>
        )}
      </div>
    </div>
  );
}

